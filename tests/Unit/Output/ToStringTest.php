<?php

use Worksome\Number\Number;

test('Number can convert to string', function (string|float|int $input) {
    $expect = (string) $input;
    $result = Number::of($input)->toString();

    expect($result)->toBe($expect);
})->with([
    '3487239482304823094782304982398233984.320412080039237532875320',
    '-3487239482304823094782304982398233984.320412080039237532875320',
    '3234.23423423',
    '-3234.23423423',
    '23.000',
    '-23.000',
    '10.000',
    '-10.000',
    '0.0',
    '0',
    '23',
    '-23',
    '239472374',
    '-239472374',
    '2309480385043921850238432905232309572309479235723',
    '-2309480385043921850238432905232309572309479235723',
]);
